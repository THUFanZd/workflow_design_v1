from __future__ import annotations

import argparse
import json
import random
import re
from dataclasses import asdict, dataclass
from datetime import datetime
from pathlib import Path
from typing import Callable, Dict, List, Optional, Sequence

OUTPUT_METRIC_GENERATION_PROMPTS = ["The explanation is simple:", "I think", "We"]
KL_DIV_VALUES = [0.25, 0.5, -0.25, -0.5]

OUTPUT_METRIC_SYS_PROMPT = """You are analyzing the behavior of a specific neuron in a language model. You will receive:

1. A hypothesized explanation for what concept the neuron represents (e.g., specific tokens, themes, or ideas).
2. Three sets of completions, one generated by amplifying the activation of the neuron in question, and one of a random neuron across the same prompts.

Your goal is to identify which set of completions is more likely the result of amplifying the neuron in question. To do this:
- Look for completions where the literal words or the ideas/themes described in the explanation occur more frequently or with greater emphasis.
- Remember that amplification may highlight specific words or their broader contextual meanings, meaning that a lot of the times they might be very noisy, but contain keywords that appear in the explanation.
- Your answer should be based on the content of the completions, not the quality of the language model's output.
- Your reasoning should be sound, don't make overly elaborate and far-fetched connections.

The first line in your response should be a brief explanation of your choice - what made you choose that set of completions.
The second line must be only the set number you think matches the description (i.e., 1, 2 or 3) and no other text. You must pick one of the three sets.
"""

STEERING_FULL_PROMPT_TEMPLATE = """Explanation: {explanation}

# Set 1
{amplifications1}

# Set 2
{amplifications2}

# Set 3
{amplifications3}
"""


@dataclass
class BlindTrialResult:
    correct_choice: int
    chosen_choice: int
    success: bool
    judge_response: str
    blind_prompt: str


@dataclass
class InterventionBlindScore:
    explanation: str
    score: float
    successes: int
    trials: int
    trial_results: List[BlindTrialResult]


class OpenAIChatClient:
    def __init__(self, model: str, base_url: str, api_key: str):
        from openai import OpenAI

        self.model = model
        self.client = OpenAI(base_url=base_url, api_key=api_key)

    def chat(self, messages: List[Dict[str, str]], temperature: float = 0.0, max_tokens: int = 512) -> str:
        response = self.client.chat.completions.create(
            model=self.model,
            messages=messages,
            temperature=temperature,
            max_tokens=max_tokens,
            stream=False,
            extra_body={},
        )
        content = response.choices[0].message.content
        return content if content is not None else ""


def _extract_choice(judge_response: str) -> int:
    lines = [ln.strip() for ln in judge_response.splitlines() if ln.strip()]
    pattern = re.compile(r"\b([123])\b")
    for line in reversed(lines):
        match = pattern.search(line)
        if match:
            return int(match.group(1))
    match = pattern.search(judge_response)
    if match:
        return int(match.group(1))
    raise ValueError(f"Failed to parse choice from judge response: {judge_response!r}")


def _normalize_result_text(text: str) -> str:
    return text.strip().replace("\r\n", "\n").replace("\r", "\n")


def _reverse_amplification_blocks(control_result: str) -> str:
    block_pattern = re.compile(r"(<[+-]?\d+(?:\.\d+)?>.*?)(?=(?:\n<[+-]?\d)|\Z)", re.DOTALL)
    blocks = [b.strip() for b in block_pattern.findall(control_result) if b.strip()]
    if len(blocks) < 2:
        return control_result
    return "\n".join(reversed(blocks))


def _pick_controls(control_results: Sequence[str]) -> List[str]:
    controls = [_normalize_result_text(x) for x in control_results if x and x.strip()]
    if not controls:
        raise ValueError("At least one control result is required.")
    if len(controls) >= 2:
        return controls[:2]
    return [controls[0], _reverse_amplification_blocks(controls[0])]


def generate_explanation_from_result(intervention_result: str) -> str:
    payloads: List[str] = []
    for line in intervention_result.splitlines():
        if "': '" in line:
            payloads.append(line.split("': '", 1)[1].strip().strip("'"))
    text = " ".join(payloads).lower()
    if any(x in text for x in ["marine", "ocean", "coastal", "wetland", "fishing"]):
        return "Related to marine ecological protection, climate change impacts, and mitigation actions."
    if any(x in text for x in ["court", "legal", "plaintiff", "defendant", "motion", "appeal"]):
        return "Related to legal procedure, courtroom discourse, and judicial decision language."
    if any(x in text for x in ["gene", "protein", "cell", "assay", "biomarker", "mutation"]):
        return "Related to biomedical and molecular biology research language."
    return "This SAE feature appears to steer completions toward a coherent semantic theme."


def score_intervention_explanation(
    explanation: str,
    intervention_result: str,
    control_results: Sequence[str],
    *,
    trials: int = 1,
    seed: int = 42,
    judge_fn: Callable[[str, Sequence[str]], str],
) -> InterventionBlindScore:
    if trials <= 0:
        raise ValueError("trials must be a positive integer.")

    intervention = _normalize_result_text(intervention_result)
    controls = _pick_controls(control_results)
    rng = random.Random(seed)

    trial_results: List[BlindTrialResult] = []
    for _ in range(trials):
        options = [controls[0], controls[1], intervention]
        rng.shuffle(options)
        correct_choice = options.index(intervention) + 1

        blind_prompt = STEERING_FULL_PROMPT_TEMPLATE.format(
            explanation=explanation.strip(),
            amplifications1=options[0],
            amplifications2=options[1],
            amplifications3=options[2],
        )

        judge_response = judge_fn(blind_prompt, options)
        try:
            chosen_choice = _extract_choice(judge_response)
        except ValueError:
            chosen_choice = -1

        trial_results.append(
            BlindTrialResult(
                correct_choice=correct_choice,
                chosen_choice=chosen_choice,
                success=(chosen_choice == correct_choice),
                judge_response=judge_response,
                blind_prompt=blind_prompt,
            )
        )

    successes = sum(1 for t in trial_results if t.success)

    return InterventionBlindScore(
        explanation=explanation,
        score=successes / trials,
        successes=successes,
        trials=trials,
        trial_results=trial_results,
    )


def format_intervention_result(
    completions_by_kl: Dict[float, Sequence[str]],
    prompts: Sequence[str] = OUTPUT_METRIC_GENERATION_PROMPTS,
) -> str:
    lines: List[str] = []
    for kl in KL_DIV_VALUES:
        completions = list(completions_by_kl[kl])
        if len(completions) != len(prompts):
            raise ValueError(
                f"KL={kl} completion size mismatch: got {len(completions)}, expected {len(prompts)}."
            )
        amp_tag = f"{kl:+g}"
        for i, prompt in enumerate(prompts):
            completion = completions[i].replace("\n", "\\n").replace("\r", "\\r")
            lines.append(f"<{amp_tag}>'{prompt}': '{completion}'")
    return "\n".join(lines)


def _infer_sae_lens_uri_from_local_path(sae_path: str) -> Optional[str]:
    p = Path(sae_path)
    parts = list(p.parts)
    model_part_idx = -1
    for i, part in enumerate(parts):
        if part.startswith("models--"):
            model_part_idx = i
            break
    if model_part_idx < 0:
        return None

    release = parts[model_part_idx].replace("models--", "").replace("--", "/")
    remainder = parts[model_part_idx + 1 :]
    if len(remainder) >= 2 and remainder[0] == "snapshots":
        remainder = remainder[2:]
    if not remainder:
        return None
    sae_id = "/".join(remainder).replace("\\", "/")
    return f"sae-lens://release={release};sae_id={sae_id}"


def build_intervention_result_from_checkpoint(
    *,
    model_checkpoint_path: str,
    sae_checkpoint_path: str,
    feature_index: int,
    device: str = "cpu",
    prompts: Sequence[str] = OUTPUT_METRIC_GENERATION_PROMPTS,
    max_new_tokens: int = 25,
    temperature: float = 0.75,
) -> str:
    from model_with_sae import ModelWithSAEModule

    sae_path = sae_checkpoint_path
    if not sae_path.startswith("sae-lens://"):
        inferred_uri = _infer_sae_lens_uri_from_local_path(sae_path)
        if inferred_uri is None:
            raise ValueError(
                "sae_checkpoint_path is not sae-lens URI and could not infer one from path. "
                "Please pass sae-lens://release=...;sae_id=..."
            )
        sae_path = inferred_uri

    module = ModelWithSAEModule(
        llm_name=model_checkpoint_path,
        sae_path=sae_path,
        feature_index=int(feature_index),
        device=device,
    )

    completions_by_kl: Dict[float, Sequence[str]] = {}
    for kl in KL_DIV_VALUES:
        out = module.generate_steered_completions(
            prompts=list(prompts),
            feature_index=int(feature_index),
            max_new_tokens=max_new_tokens,
            temperature=temperature,
            target_kl=float(kl),
        )
        completions = out.get("steered_completion")
        if not isinstance(completions, list):
            raise RuntimeError(f"Unexpected steered output format for KL={kl}: {out}")
        completions_by_kl[kl] = completions

    return format_intervention_result(completions_by_kl, prompts=prompts)


def _load_text(path: Path) -> str:
    return path.read_text(encoding="utf-8").strip()


def _resolve_api_key(api_key: Optional[str], api_key_file: Optional[str]) -> Optional[str]:
    if api_key:
        return api_key
    if api_key_file:
        key_path = Path(api_key_file)
        if key_path.exists():
            return key_path.read_text(encoding="utf-8").strip()
    return None


def _build_judge_fn(
    *,
    api_key: Optional[str],
    api_key_file: Optional[str],
    model: str,
    base_url: str,
) -> Callable[[str, Sequence[str]], str]:
    key = _resolve_api_key(api_key=api_key, api_key_file=api_key_file)
    if not key:
        raise ValueError("Missing API key: set --api-key or --api-key-file.")

    client = OpenAIChatClient(model=model, base_url=base_url, api_key=key)

    def _judge(prompt: str, _option_sets: Sequence[str]) -> str:
        messages = [{"role": "user", "content": OUTPUT_METRIC_SYS_PROMPT + "\n\n" + prompt}]
        return client.chat(messages=messages, temperature=0.0, max_tokens=512)

    return _judge


def _save_judge_trial_logs(score_result: InterventionBlindScore, output_dir: Path = Path("./outputs")) -> List[Path]:
    output_dir.mkdir(parents=True, exist_ok=True)
    saved_paths: List[Path] = []

    for i, trial in enumerate(score_result.trial_results, start=1):
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S_%f")
        path = output_dir / f"judge_trial_{timestamp}.md"
        content = (
            f"# Judge Trial {i}\n\n"
            f"- correct_choice: {trial.correct_choice}\n"
            f"- chosen_choice: {trial.chosen_choice}\n"
            f"- success: {trial.success}\n\n"
            "## Judge Input\n\n"
            "```text\n"
            f"{OUTPUT_METRIC_SYS_PROMPT}\n\n{trial.blind_prompt}\n"
            "```\n\n"
            "## Judge Output\n\n"
            "```text\n"
            f"{trial.judge_response}\n"
            "```\n"
        )
        path.write_text(content, encoding="utf-8")
        saved_paths.append(path)
    return saved_paths


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Three-way blind-test scoring for SAE intervention explanations."
    )
    parser.add_argument("--example-dir", type=str, default=".")
    parser.add_argument("--exp-file", type=str, default="intervention_example_1.txt")
    parser.add_argument(
        "--control-files",
        nargs="*",
        default=["intervention_example_2.txt", "intervention_example_3.txt"],
    )

    parser.add_argument("--explanation", type=str, default=None)
    parser.add_argument("--trials", type=int, default=1)
    parser.add_argument("--seed", type=int, default=42)

    parser.add_argument("--api-key", type=str, default=None)
    parser.add_argument("--api-key-file", type=str, default=None)
    parser.add_argument("--openai-model", type=str, default="zai-org/glm-4.7")
    parser.add_argument("--openai-base-url", type=str, default="https://api.ppio.com/openai")

    parser.add_argument("--use-checkpoint", action="store_true")
    parser.add_argument("--model-checkpoint-path", type=str, default=None)
    parser.add_argument("--sae-checkpoint-path", type=str, default=None)
    parser.add_argument("--feature-index", type=int, default=None)
    parser.add_argument("--device", type=str, default="cpu")

    parser.add_argument("--verbose", action="store_true")
    parser.add_argument("--output-json", type=str, default=None)
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    example_dir = Path(args.example_dir)

    if args.use_checkpoint:
        if args.model_checkpoint_path is None or args.sae_checkpoint_path is None or args.feature_index is None:
            raise ValueError(
                "--use-checkpoint requires --model-checkpoint-path, --sae-checkpoint-path and --feature-index."
            )
        intervention_result = build_intervention_result_from_checkpoint(
            model_checkpoint_path=args.model_checkpoint_path,
            sae_checkpoint_path=args.sae_checkpoint_path,
            feature_index=args.feature_index,
            device=args.device,
            prompts=OUTPUT_METRIC_GENERATION_PROMPTS,
        )
    else:
        intervention_result = _load_text(example_dir / args.exp_file)

    control_results = [_load_text(example_dir / f) for f in args.control_files]

    explanation = args.explanation or generate_explanation_from_result(intervention_result)
    
    judge_fn = _build_judge_fn(
        api_key=args.api_key,
        api_key_file=args.api_key_file,
        model=args.openai_model,
        base_url=args.openai_base_url,
    )

    score_result = score_intervention_explanation(
        explanation=explanation,
        intervention_result=intervention_result,
        control_results=control_results,
        trials=int(args.trials),
        seed=int(args.seed),
        judge_fn=judge_fn,
    )

    print("Explanation:")
    print(score_result.explanation)
    print()
    print(f"Score: {score_result.score:.3f} ({score_result.successes}/{score_result.trials})")

    saved_trial_logs = _save_judge_trial_logs(score_result)
    for p in saved_trial_logs:
        print(f"Saved judge trial log: {p}")

    if args.verbose:
        print()
        print("Trial details:")
        for i, t in enumerate(score_result.trial_results, start=1):
            print(f"[Trial {i}] correct={t.correct_choice}, chosen={t.chosen_choice}, success={t.success}")
            print("Judge response:")
            print(t.judge_response)

    if args.output_json:
        output_path = Path(args.output_json)
        output_path.write_text(
            json.dumps(asdict(score_result), ensure_ascii=False, indent=2),
            encoding="utf-8",
        )
        print(f"Saved JSON result to: {output_path}")


if __name__ == "__main__":
    main()
